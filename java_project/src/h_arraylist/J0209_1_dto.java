package h_arraylist;

public class J0209_1_dto {
	private String name, gender;
	private int pay, age;
	
	public J0209_1_dto(String name, String gender, int pay, int age) {
		super();
		this.name = name;
		this.gender = gender;
		this.pay = pay;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public int getPay() {
		return pay;
	}

	public void setPay(int pay) {
		this.pay = pay;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	
}

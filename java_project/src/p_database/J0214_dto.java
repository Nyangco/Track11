package p_database;

public class J0214_dto {

	private String id,name,area;
	private int age,num;
	
	public J0214_dto(String id, String name, String area, int age, int num) {
		super();
		this.id = id;
		this.name = name;
		this.area = area;
		this.age = age;
		this.num = num;
	}

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getArea() {
		return area;
	}

	public int getAge() {
		return age;
	}

	public int getNum() {
		return num;
	}
	
	
	
	
	
	
}
